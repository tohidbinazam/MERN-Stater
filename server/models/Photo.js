import { Schema, model } from 'mongoose';

const photoSchema = Schema(
  {
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const Photo = model('Photo', photoSchema);

export default Photo;
